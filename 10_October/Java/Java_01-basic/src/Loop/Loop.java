package Loop;

public class Loop {
    public static void main(String[] args) {
        /* for문 */
        System.out.println("for문");
        for (int i=1; i<=10; i++) {
            System.out.print(i+" ");
        }

        System.out.println();

        /* while문 */
        System.out.println("while문");
        int i = 1;
        while (i<=10) {
            System.out.print(i+" ");
            i++;
        }
    }
}
